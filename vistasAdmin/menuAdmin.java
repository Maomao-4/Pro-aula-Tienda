    public void Listar() {
        
        String SentenciaSQL = "SELECT Nombre, etiqueta, tipo, Precio, Stock FROM Productos";
        try {
            Conexion co = new Conexion();
            ResultSet resultado = co.consultar(SentenciaSQL);
            Vector<Vector> datosTabla = new Vector();
            Vector<String> columnas = new Vector();
            columnas.add("Nombre");
            columnas.add("Etiqueta");
            columnas.add("Tipo");
            columnas.add("Precio");
            columnas.add("Stock");
            DefaultTableModel modeloTabla = new DefaultTableModel();
            while (resultado.next()) {
                Vector<String> fila = new Vector();
                fila.add(resultado.getString("Nombre"));
                fila.add(resultado.getString("etiqueta"));
                fila.add(resultado.getString("tipo"));
                fila.add(resultado.getString("Precio"));
                fila.add(resultado.getString("Stock"));
                datosTabla.add(fila);
            }
            if (datosTabla.isEmpty()) {
                JOptionPane.showMessageDialog(this, "No existen productos en la BD");
                return;
            }
            modeloTabla.setDataVector(datosTabla, columnas);
            this.tblEmpleados.setModel(modeloTabla);
        } catch (Exception error) {
            JOptionPane.showMessageDialog(this, "ERROR AL LISTAR");
            error.printStackTrace();
        }
    }
    
    public void mos() {
    int fila = tblEmpleados.getSelectedRow();
    
    if (fila != -1) { // Verifica que se haya seleccionado una fila v√°lida
        
        String Nombre = tblEmpleados.getValueAt(fila, 0) != null ? tblEmpleados.getValueAt(fila, 0).toString() : "";
        int etiqueta = tblEmpleados.getValueAt(fila, 1) != null ? Integer.parseInt(tblEmpleados.getValueAt(fila, 1).toString()) : 0;
        String tipo = tblEmpleados.getValueAt(fila, 2) != null ? tblEmpleados.getValueAt(fila, 2).toString() : "";
        float precio = tblEmpleados.getValueAt(fila, 3) != null ? Float.parseFloat(tblEmpleados.getValueAt(fila, 3).toString()) : 0;
        int stock = tblEmpleados.getValueAt(fila, 4) != null ? Integer.parseInt(tblEmpleados.getValueAt(fila, 4).toString()) : 0;
        
        InformacionP pframe = new InformacionP(etiqueta, Nombre, tipo, precio, stock);
        pframe.setVisible(true);
    } else {
        // Maneja el caso en el que no se haya seleccionado ninguna fila
    }
}
    
